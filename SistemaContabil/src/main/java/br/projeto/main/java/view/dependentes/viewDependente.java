    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        final int tabela = jTableDependente.getSelectedRow();

        if (tabela < 0) {
            JOptionPane.showMessageDialog(null, "Selecione uma dependente!");
        } else {

            String nome = (String) jTableDependente.getValueAt(tabela, 2);
            final int id = (int) jTableDependente.getValueAt(tabela, 0);

            if (JOptionPane.showConfirmDialog(null, "Deseja excluir cadastro da " + nome + "?") == JOptionPane.YES_OPTION) {

                try {

                    Dependente dep = new Dependente();
                    dep.setId(id);
                    new DependenteDAO().apagar(dep);
                    preencherGrid(new DependenteDAO().buscarTodos());

                } catch (SQLException ex) {
                    JOptionPane.showConfirmDialog(null, "Erro Driver não encontrado !");
                } catch (ClassNotFoundException ex) {
                    JOptionPane.showConfirmDialog(null, "Erro na conexão com o Bancou ou na inserção de dados.");
                }
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

