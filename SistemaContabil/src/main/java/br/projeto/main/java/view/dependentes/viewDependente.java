    private void preencherGrid(ArrayList<Dependente> lista) {

        Object[] columnNames = {"ID", "EMPREGADO RESPONSAVEL", "NOME", "CPF"};
        DefaultTableModel dtm = new DefaultTableModel(columnNames, 0);

        for (Dependente em : lista) {
            Object[] values = {em.getId(), em.getResponsavel().getNome(), em.getNome(), em.getCpf()};
            dtm.addRow(values);
        }

        getjTableDependente().setModel(dtm);
    }

    public JTable getjTableDependente() {
        return jTableDependente;
    }
