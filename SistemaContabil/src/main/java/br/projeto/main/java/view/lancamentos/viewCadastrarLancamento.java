    void alterarRegistro(Lancamento lanc) {
        jTfinsertEmpresa.setText(String.valueOf(lanc.getEmpresa().getId()));
        JTFCodigo.setText(String.valueOf(lanc.getId()));
        jTfInsertCredito.setText(String.valueOf(lanc.getCredito()));
        jtfInsertDebito.setText(String.valueOf(lanc.getDebito()));
        jTfInsertDescricaoLancamento.setText(lanc.getDescricao());
        JTFPatrimonio.setText(String.valueOf(lanc.getPatrimonioTotal()));
        jTfData.setText(FormatFactory.formatDate(lanc.getDataLancamento()));
        jTfinsertEmpresa.setEnabled(false);
    }

