    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        Lancamento lanc = new Lancamento();

        Empresa em = new Empresa();
        em.setId(Integer.parseInt(jTfinsertEmpresa.getText()));

        lanc.setCredito(Double.parseDouble(jTfInsertCredito.getText()));
        lanc.setDebito(Double.parseDouble(jtfInsertDebito.getText()));
        lanc.setDescricao(jTfInsertDescricaoLancamento.getText());
        lanc.setPatrimonioTotal(Double.parseDouble(JTFPatrimonio.getText()));
        lanc.setEmpresa(em);
        lanc.setDataLancamento(FormatFactory.convertToDate(jTfData.getText()));

        try {
            if (JTFCodigo.getText().isEmpty() == false) {
                lanc.setId(Integer.parseInt(JTFCodigo.getText()));
                new LancamentoDAO().alterar(lanc);
                
            } else if (JTFCodigo.getText().isEmpty() == true) {
                new LancamentoDAO().inserir(lanc);
                
            }

            setVisible(false);
            
        } catch (SQLException ex) {
            JOptionPane.showConfirmDialog(null, "Erro Driver não encontrado ! ");
        } catch (ClassNotFoundException ex) {
            JOptionPane.showConfirmDialog(null, "Erro na conexão com o Bancou ou na inserção de dados.");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

