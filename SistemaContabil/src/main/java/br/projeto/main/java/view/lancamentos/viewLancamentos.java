    private void preencherGrid(ArrayList<Lancamento> lista) {
        
        Object[] columnNames = {"ID","NOME EMPRESA" ,"PATRIMONIO_TOTAL", "DEBITO", "CREDITO","DESCRICAO"};
        DefaultTableModel dtm = new DefaultTableModel(columnNames, 0);

        for (Lancamento em : lista) {
            Object[] values = {em.getId(),em.getEmpresa().getNome(),"R$ "+em.getPatrimonioTotal(), "R$ "+em.getDebito(), "R$ "+em.getCredito(),em.getDescricao()};
            dtm.addRow(values);
        }

        getjTableLancamento().setModel(dtm);
    }
    
