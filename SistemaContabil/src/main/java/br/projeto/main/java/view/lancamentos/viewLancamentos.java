    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
       
         final int tabela = jTableLancamento.getSelectedRow();

        if (tabela < 0) {
            JOptionPane.showMessageDialog(null, "Selecione uma lançamento!");
        } else {

           final String nome = (String) jTableLancamento.getValueAt(tabela, 1);
           final int id = (int) jTableLancamento.getValueAt(tabela, 0);

            if (JOptionPane.showConfirmDialog(null, "Deseja excluir cadastro do lançamento de "+nome+"?") == JOptionPane.YES_OPTION) {

                try {
                    
                    Lancamento lanc = new Lancamento();
                    lanc.setId(id);
                    new LancamentoDAO().apagar(lanc);
                    preencherGrid(new LancamentoDAO().buscarTodos());
                    
                } catch (SQLException ex) {
                    JOptionPane.showConfirmDialog(null, "Erro Driver não encontrado !");
                } catch (ClassNotFoundException ex) {
                    JOptionPane.showConfirmDialog(null, "Erro na conexão com o Bancou ou na inserção de dados.");
                }
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        
        
        final int tabela = jTableLancamento.getSelectedRow();

        if (tabela < 0) {
            JOptionPane.showMessageDialog(null, "Selecione um lançamento!");
        } else {
            String nome = (String) jTableLancamento.getValueAt(tabela, 1);
            int id = (int) jTableLancamento.getValueAt(tabela, 0);

            try {
                
                Lancamento lanc = new LancamentoDAO().buscarPelaChave(id);
                
                viewCadastrarLancamento vcl = new viewCadastrarLancamento(null, true);
                
                ArrayList<Lancamento> lista = new LancamentoDAO().buscarTodos();
                
                vcl.alterarRegistro(lanc);
                
                vcl.setVisible(true);
                
                preencherGrid(new LancamentoDAO().buscarTodos());

            } catch (SQLException ex) {
                JOptionPane.showConfirmDialog(null, "Erro Driver não encontrado !");
            } catch (ClassNotFoundException ex) {
                JOptionPane.showConfirmDialog(null, "Erro na conexão com o Bancou ou na inserção de dados.");
            }

        }
    }//GEN-LAST:event_jButton3ActionPerformed

