    private void buttonEmpresaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonEmpresaActionPerformed

        viewEmpresa ve = new viewEmpresa(null, false);

        try {

            ArrayList<Empresa> lista = new EmpresaDAO().buscarTodos();

            String[] columnNames = {"ID", "NOME", "RESPONSAVEL", "CPNJ"};
            DefaultTableModel dtm = new DefaultTableModel(columnNames, 0);

            for (Empresa em : lista) {
                Object[] values = {em.getId(), em.getNome(), em.getResponsavel(), em.getCnpj()};
                dtm.addRow(values);
            }

            ve.getjTableEmpresa().setModel(dtm);

            ve.setVisible(true);

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro Driver n√£o encontrado! ");
        } catch (ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(null, "Erro na consulta dos dadous ou conex√£o com banco");
        }
    }//GEN-LAST:event_buttonEmpresaActionPerformed

