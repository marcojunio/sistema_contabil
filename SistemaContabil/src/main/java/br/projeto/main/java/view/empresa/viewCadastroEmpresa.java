    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        Empresa empresa = new Empresa(0, jTfInserirNomeEmpresa.getText(), jTfInserirEndereco.getText(), jTfInserirCnpjEmpresa.getText(), jTfInserirEmail.getText(), jTfInserirResponsavel.getText(), jRbtnAtiva.isSelected());
 
        if (jRbtnAtiva.isSelected() == true) {
            empresa.setEmpresaAtiva(true);
        } else {
            empresa.setEmpresaAtiva(false);
        }

        try {

            if (jTfIDEmpresa.getText().isEmpty() == false) {
                empresa.setId(Integer.parseInt(jTfIDEmpresa.getText()));
                new EmpresaDAO().alterar(empresa);
            }
            else if(jTfIDEmpresa.getText().isEmpty() == true){
                new EmpresaDAO().inserir(empresa);
            }
            
            
            setVisible(false);
            
        } catch (SQLException ex) {
            JOptionPane.showConfirmDialog(null, "Erro Driver nÃ£o encontrado !");
        } catch (ClassNotFoundException ex) {
            JOptionPane.showConfirmDialog(null, "Erro na conexÃ£o com o Bancou ou na inserÃ§Ã£o de dados.");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

