    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        Dependente dep = new Dependente();
        Empregado emp = new Empregado();

        emp.setId(Integer.parseInt(jTfInsertFuncionarioResponsavel.getText()));
        dep.setCpf(jTfInsertCpfDependente.getText());
        dep.setEndereco(jTfInsertEnderecoDependente.getText());
        dep.setDataNascimento(FormatFactory.convertToDate(jTfInsertDataNasDependente.getText()));
        dep.setFalecido(radioBtnIsFalecido1.isSelected() == true);
        dep.setIdade(Integer.parseInt(JTFIdade.getText()));
        dep.setNome(jTfInsertNomeDependente.getText());

        if (radioSexoMasculino.isSelected() == true) {
            dep.setSexo(SexoENUM.MASCULINO);
        } else {
            dep.setSexo(SexoENUM.FEMININO);
        }
        dep.setResponsavel(emp);

        try {

            if (JTFCodigo.getText().isEmpty() == false) {
                
                dep.setId(Integer.parseInt(JTFCodigo.getText()));
                new DependenteDAO().alterar(dep);
                
            } else if (JTFCodigo.getText().isEmpty() == true) {
                new DependenteDAO().inserir(dep);
            }

            setVisible(false);

        } catch (SQLException ex) {
            JOptionPane.showConfirmDialog(null, "Erro Driver nÃ£o encontrado ! " + ex.getMessage());
        } catch (ClassNotFoundException ex) {
            JOptionPane.showConfirmDialog(null, "Erro na conexÃ£o com o Bancou ou na inserÃ§Ã£o de dados.");
        }

    }//GEN-LAST:event_jButton2ActionPerformed

