        void alterarRegistro(Dependente dep) {
        
        jTfInsertFuncionarioResponsavel.setText(String.valueOf(dep.getResponsavel().getId()));
        jTfInsertDataNasDependente.setText(FormatFactory.formatDate(dep.getDataNascimento()));
        JTFCodigo.setText(String.valueOf(dep.getId()));
        jTfInsertCpfDependente.setText(dep.getCpf());
        jTfInsertEnderecoDependente.setText(dep.getEndereco());
        JTFIdade.setText(String.valueOf(dep.getIdade()));
        jTfInsertNomeDependente.setText(dep.getNome());
        radioBtnIsFalecido1.setSelected(dep.isFalecido());

        if (dep.getSexo().equals(SexoENUM.MASCULINO)) {
            radioSexoMasculino.setSelected(true);
        } else {
            radioSexoFeminino.setSelected(true);
        }

       jTfInsertFuncionarioResponsavel.setEnabled(false);
       jTfInsertDataNasDependente.setEnabled(false);
       
    }

